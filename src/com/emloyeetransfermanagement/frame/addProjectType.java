        public void autoID(){
        try {
            String query = "select TOP 1 ProjectTypeID from ProjectType order by ProjectTypeID desc";
            
            pst = conn.prepareStatement(query);
            rs = pst.executeQuery();
            
            if(rs.next()){
                int t1 = rs.getInt("ProjectTypeID");
                int id = t1 + 1;
                txtProjectTypeID.setText(String.valueOf(id));
            }
                    
            } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

    
    public void hideButton(){
        ProjectTypeManagement mark = new ProjectTypeManagement();
        String marked = mark.mark();
        
        if(marked.equals("modify")){
            updateProjectType.setVisible(true);
        }else {
            updateProjectType.setVisible(false);
        }
        
        if(marked.equals("add")){
            saveProjectType.setVisible(true);
        }else{
            saveProjectType.setVisible(false);
        }
        
    }
    
    
public void RecuperProjectType(){
    ProjectTypeManagement modify = new ProjectTypeManagement();
    
    try {
        String test2 = modify.getTableResult();
        String query = "select * from ProjectType where ProjectTypeID = '"+test2+"'";
        
        pst = conn.prepareStatement(query);
        rs = pst.executeQuery();
        
        if(rs.next()){
            String t1 = rs.getString("ProjectTypeID");
            txtProjectTypeID.setText(t1);
            String t2 = rs.getString("ProjectTypeName");
            txtProjectTypeName.setText(t2);
            String t3 = rs.getString("Description");
            txtDescription.setText(t3);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error in sql");
        System.out.println(e);
    }
}

