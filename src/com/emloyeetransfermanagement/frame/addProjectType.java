    private void saveProjectTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveProjectTypeActionPerformed
        // TODO add your handling code here:
        try {
            String query = "insert into ProjectType(ProjectTypeID, ProjectTypeName, Description) values (?,?,?)";
            pst = conn.prepareStatement(query);
            
            pst.setString(1, txtProjectTypeID.getText());
            pst.setString(2, txtProjectTypeName.getText());
            pst.setString(3, txtDescription.getText());
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");
            
        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error: Saving is fail");
        }finally{
            try {
            pst.close();
            dispose(); 
            } catch (SQLException e) {
                System.out.println(e);
            }            
        }
        
    }//GEN-LAST:event_saveProjectTypeActionPerformed

    private void updateProjectTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProjectTypeActionPerformed
        try {
            String t1 = txtProjectTypeID.getText();
            String t2 = txtProjectTypeName.getText();
            String t3 = txtDescription.getText();
            
            
            String query = "update ProjectType set ProjectTypeName = '"+t2+"', Description = '"+t3+"' where ProjectTypeID = '"+t1+"'";
            pst = conn.prepareStatement(query);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Updated");
        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error, Update fail");
        } finally{
            try {
                pst.close();
                dispose();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        ProjectTypeManagement show = new ProjectTypeManagement();
        show.showRecord();
        
    }//GEN-LAST:event_updateProjectTypeActionPerformed

